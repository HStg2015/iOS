import UIKit

extension UIColor {
    
    class func bitsplsOrangeBright()->  UIColor {
        return UIColor(red: 0.996078431, green: 0.650980392, blue: 0.137254902, alpha: 1)
    }
    
    class func bitsplsOrangeDark()->  UIColor{
        return UIColor(red: 236/255, green: 155/255, blue: 31/255, alpha: 1)
    }
    
    class func veryBrightGray()->  UIColor{
        return UIColor(red: 251/255, green: 251/255, blue: 251/255, alpha: 1)
    }
    
    class func brightGray()->  UIColor{
        return UIColor(red: 238/255, green: 238/255, blue: 239/255, alpha: 1)
    }
    
    class func darkGray()->  UIColor{
        return UIColor(red: 89/255, green: 89/255, blue: 89/255, alpha: 1)
    }
    
    class func mainRed()->  UIColor{
        return UIColor(red: 214/255, green: 68/255, blue: 59/255, alpha: 1)
    }
}
